package com.turing.javase.javase5thbatch.chapter9.jdbc.model;

import java.util.Date;

/**
 *
 * @author macbook
 */
public class Actor {
    Long id;
    String name;
    String gender;
    Date birthday;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public Date getBirthday() {
        return birthday;
    }

    public void setBirthday(Date birthday) {
        this.birthday = birthday;
    }

    @Override
    public String toString() {
        return "Actor{" + "id=" + id + ", name=" + name + ", gender=" + gender + ", birthday=" + birthday + '}';
    }
    
    
}
